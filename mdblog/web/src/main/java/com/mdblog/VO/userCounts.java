public class userCounts {
    private int followerCounts;
    private int fansCounts;
    private int articleCounts;
    private int likeCounts;

    public int getFollowerCounts() {
        return followerCounts;
    }

    public void setFollowerCounts(int followerCounts) {
        this.followerCounts = followerCounts;
    }

    public int getFansCounts() {
        return fansCounts;
    }

    public void setFansCounts(int fansCounts) {
        this.fansCounts = fansCounts;
    }

    public int getArticleCounts() {
        return articleCounts;
    }

    public void setArticleCounts(int articleCounts) {
        this.articleCounts = articleCounts;
    }

    public int getLikeCounts() {
        return likeCounts;
    }

    public void setLikeCounts(int likeCounts) {
        this.likeCounts = likeCounts;
    }
}
